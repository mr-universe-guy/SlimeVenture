package com.mru.ld43.scene;

import com.simsilica.es.EntityComponent;

/**
 * 2d position
 * @author matt
 */
public class Position implements EntityComponent{
    protected final float x,y;

    public Position(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
    
}
